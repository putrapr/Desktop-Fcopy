    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        int bar = tbl_stok.getSelectedRow();
        String a = tabmode.getValueAt(bar, 0).toString();
        
        int ok = JOptionPane.showConfirmDialog(null,"hapus","Konfirmasi Dialog", JOptionPane.YES_NO_OPTION);
        if (ok==0){
            String sql = "DELETE FROM barang WHERE id_brg='"+a+"'";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null,"Data berhasil dihapus");
                tabelStok();
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null, "Data gagal dihapus"+e);
            }
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void btn_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubahActionPerformed
        
        int bar = tbl_stok.getSelectedRow();
        if (bar == -1){
            JOptionPane.showMessageDialog(null, "Pilih baris data yang ingin diubah");
            return;
        }
        String a = tabmode.getValueAt(bar, 0).toString();
        String b = tabmode.getValueAt(bar, 1).toString();
        //String c = tabmode.getValueAt(bar, 2).toString();
        String d = tabmode.getValueAt(bar, 3).toString();
        //String e = tabmode.getValueAt(bar, 4).toString();
        String f = tabmode.getValueAt(bar, 5).toString();
        //String g = tabmode.getValueAt(bar, 6).toString();
        //String h = tabmode.getValueAt(bar, 7).toString();
        
        JTextField nama = new JTextField();
        JTextField qty = new JTextField();
        JTextField harga_jual = new JTextField();
        nama.setText(b);
        qty.setText(d);
        harga_jual.setText(f);
        
        Object[] fields = {
            "Nama :", nama,
            "Qty :", qty,
            "Harga_jual :", harga_jual
        };
        int ok = JOptionPane.showConfirmDialog(null, fields, "Ubah Stok Barang",JOptionPane.OK_CANCEL_OPTION);
        if (ok==0){
            try {
                String sql = "UPDATE barang SET nama_brg=?, qty_brg=?, harga_jual_brg=? WHERE id_brg=?";
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, nama.getText());
                stat.setString(2, qty.getText());
                stat.setString(3, harga_jual.getText());
                stat.setString(4, a);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil diubah");
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null, "Data Gagal Diubah "+e);
            }
        }
        tabelStok();
    }//GEN-LAST:event_btn_ubahActionPerformed

