    protected void tabelStok(){
        Object[] Baris = {"ID Barang", "Nama Barang", "Satuan", "Qty", "Harga Beli", "Harga Jual", "Supplier", "Tanggal"};
        tabmode = new DefaultTableModel(null, Baris);
        tbl_stok.setModel(tabmode);
        String sql = "SELECT barang.id_brg, barang.nama_brg, barang.satuan_brg, "
                    + "barang.qty_brg, barang.harga_beli_brg, barang.harga_jual_brg, "
                    + "supplier.nama_sup, barang.tgl_brg "
                    + "FROM barang JOIN supplier "
                        + "ON (barang.supplier_brg = supplier.id_sup)";
        
        try {
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String a = hasil.getString("id_brg");
                String b = hasil.getString("nama_brg");
                String c = hasil.getString("satuan_brg");
                String d = hasil.getString("qty_brg");
                String e = hasil.getString("harga_beli_brg");
                String f = hasil.getString("harga_jual_brg");
                String g = hasil.getString("nama_sup");
                String h = hasil.getString("tgl_brg");
                
                String[] data={a,b,c,d,e,f,g,h};
                tabmode.addRow(data); 
            }
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Ditampilkan: " + e);
        }
    }
        

