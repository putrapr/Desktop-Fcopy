    protected void tabelPegawai() {
        Object[] Baris = {"Nama", "No. Telepon", "Alamat"};
        tabmode = new DefaultTableModel(null, Baris);
        tbl_pegawai.setModel(tabmode);
        String sql = "SELECT nama_peg, telp_peg, alamat_peg FROM pegawai";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){                
                String a = hasil.getString("nama_peg");                
                String b = hasil.getString("telp_peg");
                String c = hasil.getString("alamat_peg");                
                
                String[] data={a,b,c};
                tabmode.addRow(data);
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Tabel tidak bisa ditampilkan"+e);
        }
    }
    
    protected boolean cekNama(String nama){
        boolean z = false;
        nama = tf_nama.getText();
        String sql = "SELECT nama_peg FROM pegawai WHERE nama_peg = '"+nama+"'";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            String a = "";
            while(hasil.next()){
                a = hasil.getString("nama_peg");
            }
            if(a.equals(nama)) {                
                z = true;
            }            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Gagal ambil nama : "+e);
        }
        return z;
    }
    
    protected int getId(){
        int id = 0;
        String sql = "SELECT id_peg FROM pegawai WHERE nama_peg = '"+name+"'";
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                id = hasil.getInt("id_peg");
            }            
            
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, "Gagal ambil id : "+e);
        }
        return id;
    }
    
