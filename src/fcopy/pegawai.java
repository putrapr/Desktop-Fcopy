    private void tbl_pegawaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_pegawaiMouseClicked
        
        int bar = tbl_pegawai.getSelectedRow();
        String nama = tabmode.getValueAt(bar, 0).toString();
        this.name = nama;
        String sql = "SELECT nama_peg, tmpt_lahir_peg, tgl_lahir_peg, jns_kel_peg, alamat_peg, telp_peg FROM pegawai WHERE nama_peg = '"+nama+"'";
        
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);            
            String a = "", b = "", d = "", e = "", f = "", g = "";
            
            while(hasil.next()){
                a = hasil.getString("nama_peg");
                b = hasil.getString("tmpt_lahir_peg");
                Date c = hasil.getDate("tgl_lahir_peg");
                d = hasil.getString("jns_kel_peg");
                e = hasil.getString("alamat_peg");
                f = hasil.getString("telp_peg");
                date_tgl_lahir.setDate(c);                
            }
            tf_nama.setText(a);
            tf_tmpt_lahir.setText(b);
            switch (d) {
                case "Pria":
                    rb_pria.setSelected(true);
                    rb_wanita.setSelected(false);
                    break;
                case "Wanita":
                    rb_pria.setSelected(false);
                    rb_wanita.setSelected(true);
                    break;
                default:
                    rb_pria.setSelected(false);
                    rb_wanita.setSelected(false);
                    break;
            }
            ta_alamat.setText(e);
            tf_no_telp.setText(f);
        }catch(SQLException ee){
            JOptionPane.showMessageDialog(null, "Data gagal diambil"+ee);
        }
    }//GEN-LAST:event_tbl_pegawaiMouseClicked

