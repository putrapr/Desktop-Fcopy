    private void tabelSupplier(){
        Object[] Baris = {"Nama", "No. Telepon", "Alamat" , "Keterangan"};
        tabmode = new DefaultTableModel(null, Baris);
        tbl_supplier.setModel(tabmode);
        String sql = "SELECT nama_sup, telp_sup, alamat_sup, ket_sup FROM supplier";
        try {
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String a = hasil.getString("nama_sup");
                String b = hasil.getString("telp_sup");
                String c = hasil.getString("alamat_sup");
                String d = hasil.getString("ket_sup");               
                
                String[] data={a,b,c,d};
                tabmode.addRow(data); 
            }
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Ditampilkan: " + e);
        }
        tbl_supplier.getColumnModel().getColumn(2).setCellRenderer(new WordWrapCellRenderer());
    }
    
    protected boolean cekNama(String nama){
        boolean z = false;
        nama = tf_nama.getText();
        String sql = "SELECT nama_sup FROM supplier WHERE nama_sup = '"+nama+"'";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            String a = "";
            while(hasil.next()){
                a = hasil.getString("nama_sup");
            }
            if(a.equals(nama)) {                
                z = true;
            }            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Gagal ambil nama"+e);
        }
        return z;
    }
    
    protected int getId(){
        int id = 0;
        String sql = "SELECT id_sup FROM supplier WHERE nama_sup = '"+name+"'";
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                id = hasil.getInt("id_sup");
            }                
            
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, "Gagal ambil id : "+e);
        }
        return id;
    }
