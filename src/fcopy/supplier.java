    private void btn_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubahActionPerformed
        int id = getId();
        String nama = tf_nama.getText();
        
        if (nama.equals("")){
            JOptionPane.showMessageDialog(null, "Nama kosong !. Silahkan isi terlebih dahulu");
            return;
        }
        
        String sql = "UPDATE supplier SET nama_sup=?, alamat_sup=?, telp_sup=?, ket_sup=? "
                    + "WHERE id_sup=?";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, nama);
            stat.setString(2, ta_alamat.getText());
            stat.setString(3, tf_no_telp.getText());
            stat.setString(4, ta_keterangan.getText());
            stat.setInt(5, id);
            
            stat.executeUpdate();
            clear();
            tabelSupplier();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Diubah "+e);
        }
    }//GEN-LAST:event_btn_ubahActionPerformed

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        int id = getId();
        int ok = JOptionPane.showConfirmDialog(null,"hapus","Konfirmasi Dialog", JOptionPane.YES_NO_CANCEL_OPTION);
        if (ok==0){
            String sql = "DELETE FROM supplier WHERE id_sup='"+id+"'";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.executeUpdate();
                clear();
                tabelSupplier();
                
            }catch(SQLException e){
                JOptionPane.showMessageDialog(null, "Data gagal dihapus"+e);
            }
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

